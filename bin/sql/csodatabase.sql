CREATE TABLE IF NOT EXISTS users (
	userID INT UNSIGNED NOT NULL AUTO_INCREMENT,
	userName VARCHAR(16) NOT NULL,
	password BINARY(60) NOT NULL,
	registerTime DATETIME NOT NULL,
	PRIMARY KEY (userID)
);

CREATE TABLE IF NOT EXISTS user_characters (
	userID INT UNSIGNED NOT NULL,
	unk1 TINYINT UNSIGNED DEFAULT 0,
	nickName VARBINARY(16) NOT NULL,
	unk4_1 TEXT DEFAULT '',
	unk4_2 TINYINT UNSIGNED DEFAULT 0,
	unk4_3 TINYINT UNSIGNED DEFAULT 0,
	unk4_4 TINYINT UNSIGNED DEFAULT 0,
	level TINYINT UNSIGNED DEFAULT 1,
	unk10 TINYINT UNSIGNED DEFAULT 0,
	exp BIGINT UNSIGNED DEFAULT 0,
	cash BIGINT UNSIGNED DEFAULT 0,
	points BIGINT UNSIGNED DEFAULT 0,
	battles INT UNSIGNED DEFAULT 0,
	wins INT UNSIGNED DEFAULT 0,
	frags INT UNSIGNED DEFAULT 0,
	deaths INT UNSIGNED DEFAULT 0,
	city SMALLINT UNSIGNED DEFAULT 0,
	county SMALLINT UNSIGNED DEFAULT 0,
	neighborhood SMALLINT UNSIGNED DEFAULT 0,
	unk200_5 TEXT DEFAULT '',
	unk400 INT UNSIGNED DEFAULT 0,
	unk800 TINYINT UNSIGNED DEFAULT 0,
	clanID INT UNSIGNED DEFAULT 0,
	clanMarkID INT UNSIGNED DEFAULT 0,
	clanName TEXT DEFAULT '',
	clanUnk4 TINYINT DEFAULT 0,
	clanUnk5 TINYINT DEFAULT 0,
	clanUnk6 TINYINT DEFAULT 0,
	PRIMARY KEY (userID),
	FOREIGN KEY (userID) REFERENCES users(userID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS user_options (
	userID INT UNSIGNED NOT NULL,
	userOption BLOB NOT NULL,
	PRIMARY KEY (userID),
	FOREIGN KEY (userID) REFERENCES users(userID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS user_buymenus (
	userBuyMenuID INT UNSIGNED NOT NULL AUTO_INCREMENT,
	userID INT UNSIGNED NOT NULL,
	categoryID TINYINT UNSIGNED,
	slot1 TINYINT UNSIGNED,
	slot2 TINYINT UNSIGNED,
	slot3 TINYINT UNSIGNED,
	slot4 TINYINT UNSIGNED,
	slot5 TINYINT UNSIGNED,
	slot6 TINYINT UNSIGNED,
	slot7 TINYINT UNSIGNED,
	slot8 TINYINT UNSIGNED,
	slot9 TINYINT UNSIGNED,
	PRIMARY KEY (userBuyMenuID),
	FOREIGN KEY (userID) REFERENCES users(userID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS user_bookmarks (
	userBookMarkID INT UNSIGNED NOT NULL AUTO_INCREMENT,
	userID INT UNSIGNED NOT NULL,
	slotID TINYINT UNSIGNED,
	name VARBINARY(12),
	primaryItemID TINYINT UNSIGNED,
	primaryAmmo BOOL,
	secondaryItemID TINYINT UNSIGNED,
	secondaryAmmo BOOL,
	flashbang TINYINT UNSIGNED,
	hegrenade BOOL,
	smokegrenade BOOL,
	defusekit BOOL,
	nightvision BOOL,
	kevlar TINYINT UNSIGNED,
	unk1 TINYINT UNSIGNED,
	PRIMARY KEY (userBookMarkID),
	FOREIGN KEY (userID) REFERENCES users(userID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS user_inventory (
	userInventoryID INT UNSIGNED NOT NULL AUTO_INCREMENT,
	userID INT UNSIGNED NOT NULL,
	slotID TINYINT UNSIGNED NOT NULL,
	itemID TINYINT UNSIGNED NOT NULL,
	count TINYINT UNSIGNED NOT NULL,
	inUse BOOL,
	obtainTime INT UNSIGNED,
	expirationTime INT UNSIGNED,
	PRIMARY KEY (userInventoryID),
	FOREIGN KEY (userID) REFERENCES users(userID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS user_sessions (
	userID INT UNSIGNED NOT NULL,
	serverID TINYINT UNSIGNED NOT NULL,
	channelID TINYINT UNSIGNED NOT NULL,
	PRIMARY KEY (userID),
	FOREIGN KEY (userID) REFERENCES users(userID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS user_transfers (
	userID INT UNSIGNED NOT NULL,
	authToken BINARY(29) NOT NULL,
	serverID TINYINT UNSIGNED NOT NULL,
	channelID TINYINT UNSIGNED NOT NULL,
	externalIP BIGINT UNSIGNED NOT NULL,
	externalHostPort SMALLINT UNSIGNED NOT NULL,
	externalGuestPort SMALLINT UNSIGNED NOT NULL,
	localIP BIGINT UNSIGNED NOT NULL,
	localHostPort SMALLINT UNSIGNED NOT NULL,
	localGuestPort SMALLINT UNSIGNED NOT NULL,
	transferTime BIGINT UNSIGNED NOT NULL,
	PRIMARY KEY (userID),
	FOREIGN KEY (userID) REFERENCES users(userID) ON DELETE CASCADE
);